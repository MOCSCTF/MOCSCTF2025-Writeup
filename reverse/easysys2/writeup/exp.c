#include <stdio.h>
#include <string.h>

void xor_string(char *str, char key){
	for (int i = 0; i<strlen(str); i++){
		str[i] = (str[i] ^ key) & 0xFF;
		printf("strXORxxxxxx: 0x%x\n", str[i]);
	}

}

int main() 
{
    char flag[] = {0xE7,0xE5,0xE9,0xF9,0xE9,0xFE,0xEC,0xD1,0xF3,0x9A,0xDF,0xF5,0xED,0xC5,0xDE,0xF5,0xFE,0xC2,0x99,0xF5,0xC1,0x99,0xF3,0xF5,0xF9,0xD3,0xD9,0x99,0xD8,0xD7};
    char key = 0xAA;
    // 异或操作
    xor_string(flag, key);

    printf("Flag:%s\n", flag);

    return 0;
}
