#include <stdio.h>
#include <string.h>

void xor_string(char *str, char key){
	for (int i = 0; i<strlen(str); i++){
		str[i] = (str[i] ^ key) & 0xFF;
		printf("strXORxxxxxx: 0x%x\n", str[i]);
	}

}

int main() 
{
    char flag[] = {0x81,0x83,0x8F,0x9F,0x8F,0x98,0x8A,0xB7,0x8B,0xFC,0xFC,0xA8,0x93,0x95,0xFC,0xB9,0x93,0x87,0x82,0xA3,0xBB,0x93,0xBF,0xB5,0xBF,0x93,0xB8,0xA4,0xA5,0xBF,0x93,0xFD,0xBF,0x93,0xAA,0xA0,0x8D,0xAB,0xB1};
    char key = 0xCC;
    // 异或操作
    xor_string(flag, key);

    printf("Flag:%s\n", flag);

    return 0;
}
